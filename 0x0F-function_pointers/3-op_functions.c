/**
 * op_add - addition
 * Description: add two integer
 * @a: first int
 * @b: second int
 * Return: the result of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - suubstraction
 * Description: substract two integer
 * @a: first int
 * @b: second int
 * Return: the result of the substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * Description: multiplie two integer
 * @a: first int
 * @b: second int
 * Return: the result of the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * Description: divide two integer
 * @a: first int
 * @b: second int
 * Return: the result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder
 * Description: get the remainder of two integer
 * @a: first int
 * @b: second int
 * Return: the remainder of the two integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}

